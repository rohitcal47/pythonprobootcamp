# subscripting
print("Hello"[0])
print("Hello"[4])
print("Hello"[-1])

# string
print("123" + "345")

# integer
print(123 + 456)

# large numbers
print(123_456_789)

# float
print(3.14159)

# boolean
print(True)
print(False)

print(len("Hello Angela"))

# check data type
print(type("Hello"))
print(type(12345))
print(type(True))
print(type(3.14159))

# type casting / type conversion
int("12345")
print(int("123")+int("345"))
